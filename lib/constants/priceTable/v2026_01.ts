// 3. 都道府県名からAreaGroupKeyを即座に見つけるための逆引き辞書
export const PREF_TO_GROUP_MAP: Record<string, AreaGroupKey> = Object.entries(AREA_GROUPS).reduce(
  (acc, [key, group]) => {
    group.prefs.forEach(pref => {
      acc[pref] = key as AreaGroupKey;
      acc[pref.replace("県", "").replace("府", "").replace("都", "")] = key as AreaGroupKey; // "東京" でも引けるように
    });
    return acc;
  },
  {} as Record<string, AreaGroupKey>
);

// 4. 指定グループの料金データを取得する関数
export const getPriceByGroup = (groupKey: AreaGroupKey, version: "v2026_01" = "v2026_01") => {
  const prefKey = GROUP_TO_PREF_KEY[groupKey];
  return (PRICE_TABLES[version].prefectures as any)[prefKey];
};

